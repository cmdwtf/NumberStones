namespace cmdwtf.NumberStones.DiceTypes
{
	/// <summary>
	/// An enumeration representing the possible results from a planar die roll
	/// </summary>
	public enum PlanarResult
	{
		/// <summary>
		/// One of the 4 blank sides of the planar die
		/// </summary>
		Nothing,
		/// <summary>
		/// The planeswalker symbol side of the planar die
		/// </summary>
		Planeswalk,
		/// <summary>
		/// The chaos symbol side of the planar die
		/// </summary>
		Chaos
	}
}
