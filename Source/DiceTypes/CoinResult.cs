namespace cmdwtf.NumberStones.DiceTypes
{
	/// <summary>
	/// An enumeration representing the possible values of a coin flip
	/// </summary>
	public enum CoinResult
	{
		/// <summary>
		/// The bottom side of the coin, and Sonic's sidekick
		/// </summary>
		/// <remarks>Never fails</remarks>
		Tails = 0,
		/// <summary>
		/// The top side of the coin
		/// </summary>
		Heads = 1
	}
}
